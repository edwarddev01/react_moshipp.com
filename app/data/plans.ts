// Función para obtener los planes de un servicio específico
export function getServicePlans(serviceId: string): Plan[] {
  return allPlans.filter((plan) => plan.serviceId === serviceId);
}

// Función para obtener un plan específico por su ID
export function getPlanById(planId: string): Plan | undefined {
  return allPlans.find((plan) => plan.id === planId);
}

// Función para obtener el plan más económico de un servicio
export function getCheapestPlan(serviceId: string): Plan | undefined {
  const servicePlans = getServicePlans(serviceId);
  if (servicePlans.length === 0) return undefined;

  return servicePlans.reduce(
    (cheapest, current) =>
      current.price < cheapest.price ? current : cheapest,
    servicePlans[0]
  );
}

// Función para obtener el plan destacado de un servicio (si existe)
export function getHighlightedPlan(serviceId: string): Plan | undefined {
  return getServicePlans(serviceId).find((plan) => plan.highlight);
}
